public class SentenciasControl {
    public static void main(String args[]){

        //if else, else if igual que siempre.
        //switch:
        var variable = 2;
        switch (variable){
            case '2': case '3':
                break;
            default:
        }
        //for, while, do while (hace por lo menos una vez) como siempre
        while(true){break;}
        for (int i = 0; i < 3; i++) { }
        do{break;}while(true);
        //La palabra continue es la opuesta al break, continua con la siguiente iteracion.
        //Se ponen labels en los break y continuo si se quiere romper un ciclo en especifico. Es como go to.
    }
}
